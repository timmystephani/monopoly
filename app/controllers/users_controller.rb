  def create
    @user = User.new(user_params)

    if @user.save
      session[:user_id] = @user.id
      flash[:notice] = 'User was successfully created.'
      redirect_to user_path @user
    else
      flash[:error] = @user.errors.full_messages.to_sentence
      redirect_to '/'
    end
    
  end

